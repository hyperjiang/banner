func text2banner(s string) string {
	ret := ""
	for i := 0; i < 6; i++ {
		for j, c := range s {
			if v, ok := fonts[c]; ok {
				ret += v[i]
				if j < len(s)-1 {
					ret += " "
				}
			} else {
				ret += fonts[' '][i]
			}
		}
		ret += "\n"
	}
	return ret
}

func main() {
	var text string
	flag.StringVar(&text, "t", "banner", "text to banner")
	flag.Parse()

	banner := text2banner(strings.ToLower(text))

	fmt.Print(banner)
}
